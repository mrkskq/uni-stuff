//Да се напише програма за пресметување на сумата на сите непарни двоцифрени броеви.
//Програмата ја печати сумата на екран во следниот формат:
//11 + 13 + 15 + 17 + … + 97 + 99 = 2475
//Програмата да се реши без користење на наредбата if

#include <iostream>
using namespace std;
int main() {
    int suma = 0, i = 11;
    suma = i;
    cout << i;
    for ( i = 13; i < 100; i+=2 ){
        cout << " + " << i;
        suma += i;
    }
    cout << " = " << suma;

    return 0;
}
