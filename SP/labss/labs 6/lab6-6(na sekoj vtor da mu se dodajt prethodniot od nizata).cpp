#include <iostream>
using namespace std;

void pecatiNiza(int a[], int n){
    for(int i=0; i<n; i++){
        if (i%2!=0){
            a[i]+=a[i-1];
        }
    }
    for(int i=0; i<n; i++){
        cout<<a[i]<< " ";
    }
}

int main() {
    int N, a[100];
    cin>>N;
    for(int i=0; i<N; i++){
        cin>>a[i];
    }
    pecatiNiza(a,N);

    return 0;
}
