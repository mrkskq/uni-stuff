#include<iostream>

using namespace std;

int main() {
    int n, a[100], k;
    cin >> n;
    for (int i=0; i < n; i++){
       cin>>a[i];
    }
    cin>>k;

    int k1=n-k; //za odnazad

    for (int i=0; i < n; i++){
        if (i==k){
            int temp=a[i];
            a[i]=a[i-1];
            a[i-1]=temp;
        }
        if (i==k1){
            int temp=a[i];
            a[i]=a[i-1];
            a[i-1]=temp;
        }
    }

    for (int i=0; i < n; i++){
        cout<<a[i]<<" ";

    }
    return 0;
}
