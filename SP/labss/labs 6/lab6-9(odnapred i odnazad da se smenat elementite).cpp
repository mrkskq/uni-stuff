//ја трансформира низата, такашто ќе се направи замена на местата помеѓу елементот на позиција k
//и неговиот претходник. Имајте во предвид дека промената треба да се направи кога низата
//ја читаме од лево кон десно И исто така во спротивен правец, од десно кон лево.

