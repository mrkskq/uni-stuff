//Да се напише програма, за која на стандарден влез се внесуваат 3 броеви. 
//Доколку броевите се сите исти или сите различни на излез да се испечати 1, во спротивно да се испечати 0.

//input            output
//7 9 7            0
//8 9 10           1

#include <iostream>
using namespace std;
int main() {
    int n1, n2, n3, pecati;
    cin >> n1 >> n2 >> n3;
    pecati = ( ( n1 == n2 && n1 == n3 && n2 == n3 ) || ( n1 != n2 && n1 != n3 && n2 != n3) );
    cout << pecati;

    return 0;
}
