//Во програмата да се внесе четирицифрем број.
//Програмата да провери дали цифрите се во растечки редослед.
//Ако се во растечки редослед да испечати 1, ако не да испечати 0.

#include <iostream>
using namespace std;
int main() {
    int n, e, d, s, i, broj;
    cin >> n;
    e = n % 10;
    d = n / 10 % 10;
    s = n / 100 % 10;
    i = n / 1000;
    broj = ( ( i < s ) && ( s < d ) && ( d < e ) );
    cout << broj;
    return 0;
}
